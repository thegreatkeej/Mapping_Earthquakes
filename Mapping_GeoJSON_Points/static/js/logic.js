// Accessing the airport GeoJSON URL
let airportData = "https://raw.githubusercontent.com/thegreatkeej/Mapping_Earthquakes/Mapping_GeoJSON_Points/majorAirports.json";
// Grabbing our GeoJSON data.
d3.json(airportData).then(function(data) {
  console.log(data);
  // Creating a GeoJSON layer with the retrieved data.
  L.geoJSON(data, {
    // We turn each feature into a marker on the map.
    pointToLayer: function(feature, latlng) {
      console.log(feature);
      return L.marker(latlng).bindPopup("<h3>" + "Airport code: " + feature.properties.faa + "</h3> <hr> <h3>" + "Airport Name: " + feature.properties.name + "</h3>");
    }
  }).addTo(map);
});
